def create_user(db: Session, user: schemas.UserCreate):
    hashed_password = oauth.get_password_hash(user.password)
    db_user = models.User(email=user.email, hashed_password=hashed_password)
    db.add(db_user)
    db.commit()
    db.refresh(db_user)
    return db_user

